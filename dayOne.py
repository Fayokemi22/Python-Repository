# ask user to choose a day from 1-7 and print the day name

day = int(input("input day"))

if day == 1:
	print("Sunday")

if day == 2:
	print("Monday")

if day == 3:
	print("Tuesday")

if day == 4:
	print("Wednesday")

if day == 5:
	print("Thursday")

if day == 6:
	print("Friday")

if day == 7:
	print("Saturday")
